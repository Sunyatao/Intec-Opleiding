package Testweek.Exercises;

public class PrintPrimitives {
    public static void main(String[] args) {

        byte byte_b = 52;
        System.out.println("byte b = " + byte_b);

        short short_s = 456;
        System.out.println("short s = " + short_s);

        int int_i = 4567998;
        System.out.println("int i = " + int_i);

        long long_l = 456456456L;
        System.out.println("long l = " + long_l);

        float float_f = 456.45F;
        System.out.println("float f = " + float_f);

        double double_d = 654.3;
        System.out.println("double d = " + double_d);

        char char_c = 'T';
        System.out.println("char c = " + char_c);

        boolean bln = true;
        System.out.println("boolean bln = " + bln);
    }
}
